print("THIS PROGRAMS CALULATE THE VOLUME AND SURFACE AREA OF SPHERE")
r = float (input("Enter The RADIUS Of Sphere:"))
print("The RADIUS Of Sphere Is :",r)
pi = float(3.142)
V = (4/3) * pi * r**3 #volume 
print ("THE VOLUME OF SPHERE IS :",V) 
S = 4 * pi * r**2   #surface area
print ("THE SURFACE AREA OF SPHERE IS :",S) 

